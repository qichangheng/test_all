# and
# 2 种运算的并且
# 对于 and 而已，一错再错， 仇家
print(1 > 2  and  3 < 4  and 4 < 9 and 8 < 10)   # 错


# or 就是或者
# 情人眼里出西施。。。 错一点点没关系，只要你不全错
print(1 > 2  or  3 < 4  or 4 < 9 or 8 < 10)

# not ==> 非， 杠精，作对的
print( not 1 > 2 )

# 实际操作过程当中
# 实战演练
# 哪个先，那个后， 运算优先级

print(not ((2 != 3 and 6 < 7) or 5 == 4) )

